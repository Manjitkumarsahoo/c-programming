#include <stdio.h>

int main() {
    int a = 10, b = 5;

    // Equal to operator
    if (a == b) {
        printf("a is equal to b\n");
    } else {
        printf("a is not equal to b\n");
    }

    // Not equal to operator
    if (a != b) {
        printf("a is not equal to b\n");
    } else {
        printf("a is equal to b\n");
    }
    return 0;
}
